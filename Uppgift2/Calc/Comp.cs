using System;

namespace Uppgift2.Calc
{
    public class Comp
    {
        public static int Add(int x, int y)
        {
            return x + y;
        }
        public static double Sub(double x, double y)
        {
            return x - y;
        }
        public static double Multi(double x, double y)
        {
            return x * y;
        }
    }
}